package com.lx.tools;

import java.util.Date;

public class ExcelRow {
	
	private Date riqi;
	private String chepaihao;
	private double shengshu;
	private double youjia;
	private int shifoupeidui;

	public Date getRiqi() {
		return riqi;
	}
	public void setRiqi(Date riqi) {
		this.riqi = riqi;
	}
	public String getChepaihao() {
		return chepaihao;
	}
	public void setChepaihao(String chepaihao) {
		this.chepaihao = chepaihao;
	}
	public double getShengshu() {
		return shengshu;
	}
	public void setShengshu(double shengshu) {
		this.shengshu = shengshu;
	}
	public double getYoujia() {
		return youjia;
	}
	public void setYoujia(double youjia) {
		this.youjia = youjia;
	}
	public int getShifoupeidui() {
		return shifoupeidui;
	}
	public void setShifoupeidui(int shifoupeidui) {
		this.shifoupeidui = shifoupeidui;
	}
	
}
